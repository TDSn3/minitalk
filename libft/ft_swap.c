#include "libft.h"

void	ft_swap(char *s)
{
	size_t	a;
	size_t	z;
	int		copy;

	a = 0;
	z = ft_strlen(s) - 1;
	while (a < z)
	{
		copy = s[a];
		s[a] = s[z];
		s[z] = copy;
		a++;
		z--;
	}
}
