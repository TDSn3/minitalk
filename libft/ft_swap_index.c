#include "libft.h"

void	ft_swap_index(char *s, int a)
{
	int	z;
	int	copy;

	z = ft_strlen(s) - 1;
	while (a < z)
	{
		copy = s[a];
		s[a] = s[z];
		s[z] = copy;
		a++;
		z--;
	}
}
